// return은 함수 실행시 반환하는 값으로, 데이터가 나가는 통로라고 생각을 하면 된다. 
function plus(a, b) {
    return a + b;
}

console.log(plus(10, 20)); // 30
console.log(plus(100, 200)); // 300

function minus(a, b) {
    return a - b;
}

console.log(minus(10, 20)); // -10
console.log(minus(100, 200)); // -100